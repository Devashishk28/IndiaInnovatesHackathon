    for i, (name, district, lat, lng, zf) in enumerate(_INTERPOLATED_RAW):
        wards.append({
            "ward_id": 21 + i,
            "ward_name": name,
            "district": district,
            "lat": lat,
            "lng": lng,
            "zone_factor": zf,
            "is_real_station": False,
        })
    return wards

